a=int(input("a: "))
b=int(input("b: "))
            
print("Is {} is greater than {}:{}".format(a,b,a>b))
print("Is {} is less than {}:{}".format(a,b,a<b))
print("Is {} equal to {}:{}".format(a,b,a==b))
print("Is {} not equal to {}:{}".format(a,b,a!=b))
print("Is {} less than or equal to {}:{}".format(a,b,a<=b))
print("Is {} greater than or equal to {}:{}".format(a,b,a>=b))  
